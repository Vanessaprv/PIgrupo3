    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      Curso curso = new Curso();
      curso.setTipoCurso(jTTipo.getText());
      curso.setModalidade(jTModalidade.getText());
      curso.setDenominacao(jTDenominacao.getText());
      curso.setHabilitacao(jTHabilitacao.getText());
      curso.setLocalOferta(jTLocalOferta.getText());
      curso.setTurnos(jTTurno.getText());
      curso.setNumVagas(jTNumVagas.getText());
      curso.setCargaHoraria(jTCargaH.getText());
      curso.setRegimeLetivo(jTRegimeLetivo.getText());
      curso.setPeriodos(jTPeriodos.getText());
      curso.setNomeCoord(jCNomeCoord.getItemAt(WIDTH));
      curso.setCpf(jTCpf.getText());
      curso.setTitulacao(jTTitulacao.getText());
      curso.setTempoDedicacao(jTTempoDedicacao.getText());
          CursoBO bo = new CursoBO();
      try {
          bo.inserir(curso);
          JOptionPane.showMessageDialog(rootPane, " Dados gerais incluídos com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
          dispose();
      } catch (NegocioException e) {
             JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Aviso", JOptionPane.WARNING_MESSAGE);
        } 
        
    }//GEN-LAST:event_jButton1ActionPerformed

