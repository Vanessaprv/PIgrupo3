
package br.iesb.meuprograma.entidades;

public class Curso {
    private int Id;
    private String tipoCurso;
    private String modalidade;
    private String denominacao;
    private String habilitacao;
    private String localOferta;
    private String turnos;
    private String  numVagas;
    private String cargaHoraria;
    private String regimeLetivo;

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }
    

    public String getTipoCurso() {
        return tipoCurso;
    }

    public void setTipoCurso(String tipoCurso) {
        this.tipoCurso = tipoCurso;
    }

    public String getModalidade() {
        return modalidade;
    }

    public void setModalidade(String modalidade) {
        this.modalidade = modalidade;
    }

    public String getDenominacao() {
        return denominacao;
    }

    public void setDenominacao(String denominacao) {
        this.denominacao = denominacao;
    }

    public String getHabilitacao() {
        return habilitacao;
    }

    public void setHabilitacao(String habilitacao) {
        this.habilitacao = habilitacao;
    }

    public String getLocalOferta() {
        return localOferta;
    }

    public void setLocalOferta(String localOferta) {
        this.localOferta = localOferta;
    }

    public String getTurnos() {
        return turnos;
    }

    public void setTurnos(String turnos) {
        this.turnos = turnos;
    }

    public String getNumVagas() {
        return numVagas;
    }

    public void setNumVagas(String numVagas) {
        this.numVagas = numVagas;
    }

    public String getCargaHoraria() {
        return cargaHoraria;
    }

    public void setCargaHoraria(String cargaHoraria) {
        this.cargaHoraria = cargaHoraria;
    }

    public String getRegimeLetivo() {
        return regimeLetivo;
    }

    public void setRegimeLetivo(String regimeLetivo) {
        this.regimeLetivo = regimeLetivo;
    }

    public String getPeriodos() {
        return periodos;
    }

    public void setPeriodos(String periodos) {
        this.periodos = periodos;
    }

    public String getNomeCoord() {
        return nomeCoord;
    }

    public void setNomeCoord(String nomeCoord) {
        this.nomeCoord = nomeCoord;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getTitulacao() {
        return titulacao;
    }

    public void setTitulacao(String titulacao) {
        this.titulacao = titulacao;
    }

    public String getTempoDedicacao() {
        return tempoDedicacao;
    }

    public void setTempoDedicacao(String tempoDedicacao) {
        this.tempoDedicacao = tempoDedicacao;
    }
    private String periodos;
    private String nomeCoord;
    private String cpf;
    private String titulacao;
    private String tempoDedicacao;
  
}
